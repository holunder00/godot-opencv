#include "opencv_utils.h"
#include <opencv2/imgproc.hpp>
#include <opencv2/core.hpp>

using namespace godot;

void CVUtils::_bind_methods() {
    ClassDB::bind_static_method("CVUtils", D_METHOD("match_template", "image", "templ", "method"), &CVUtils::match_template);
    ClassDB::bind_static_method("CVUtils", D_METHOD("get_opencv_version"), &CVUtils::get_opencv_version);

    BIND_ENUM_CONSTANT(COLOR_BGR2GRAY); BIND_ENUM_CONSTANT(COLOR_RGB2GRAY);
    BIND_ENUM_CONSTANT(COLOR_GRAY2BGR); BIND_ENUM_CONSTANT(COLOR_BGR2HSV);
    BIND_ENUM_CONSTANT(COLOR_RGB2HSV); BIND_ENUM_CONSTANT(COLOR_HSV2BGR);
    BIND_ENUM_CONSTANT(COLOR_HSV2RGB); BIND_ENUM_CONSTANT(COLOR_BGR2RGB);
    BIND_ENUM_CONSTANT(COLOR_BGR2LAB); BIND_ENUM_CONSTANT(COLOR_RGB2LAB);

    BIND_ENUM_CONSTANT(THRESH_BINARY); BIND_ENUM_CONSTANT(THRESH_BINARY_INV);
    BIND_ENUM_CONSTANT(THRESH_TRUNC); BIND_ENUM_CONSTANT(THRESH_TOZERO);
    BIND_ENUM_CONSTANT(THRESH_TOZERO_INV); BIND_ENUM_CONSTANT(THRESH_OTSU);

    BIND_ENUM_CONSTANT(MORPH_ERODE); BIND_ENUM_CONSTANT(MORPH_DILATE);
    BIND_ENUM_CONSTANT(MORPH_OPEN); BIND_ENUM_CONSTANT(MORPH_CLOSE);
    BIND_ENUM_CONSTANT(MORPH_GRADIENT); BIND_ENUM_CONSTANT(MORPH_TOPHAT);
    BIND_ENUM_CONSTANT(MORPH_BLACKHAT);

    BIND_ENUM_CONSTANT(INTER_NEAREST); BIND_ENUM_CONSTANT(INTER_LINEAR);
    BIND_ENUM_CONSTANT(INTER_CUBIC); BIND_ENUM_CONSTANT(INTER_AREA);
    BIND_ENUM_CONSTANT(INTER_LANCZOS4);

    BIND_ENUM_CONSTANT(RETR_EXTERNAL); BIND_ENUM_CONSTANT(RETR_LIST);
    BIND_ENUM_CONSTANT(RETR_CCOMP); BIND_ENUM_CONSTANT(RETR_TREE);

    BIND_ENUM_CONSTANT(CHAIN_APPROX_NONE); BIND_ENUM_CONSTANT(CHAIN_APPROX_SIMPLE);
}

CVUtils::CVUtils() {}
CVUtils::~CVUtils() {}

Ref<CVImage> CVUtils::match_template(const Ref<CVImage> &image, const Ref<CVImage> &templ, int method) {
    cv::Mat result;
    cv::matchTemplate(image->get_mat(), templ->get_mat(), result, method);
    return CVImage::_from_mat(result);
}

String CVUtils::get_opencv_version() { return String(CV_VERSION); }
